package com.rays.javabasic;

public class ElseIf {

	public static void main(String[] args) {

		int a = 15;
		int b = 55;

		if (a > b) {

			System.out.println("Maximum No = " + a);

		} else if (a == b) {

			System.out.println("maximum no is not found becouse no is equle");

		}

		else {
			System.out.println("Maximum No = " + b);
		}

	}

}
