package com.rays.javabasic;

public class IfElse {

	public static void main(String[] args) {

		int a = 15;
		int b = 15;

		if (a > b) {
			System.out.println("maximum no = " + a);
		} else {
			System.out.println("maximum no = " + b);
		}

		if (a < b) {
			System.out.println("minimum no = " + a);
		} else {
			System.out.println("minimum no = " + b);
		}

	}

}
