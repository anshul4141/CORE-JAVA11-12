package com.rays.javabasic;

public class Operators {

	// = Assignment operator

	/**
	 * Assignment operator (`=`) is used to assign the value of the right-hand
	 * operand to the left-hand operand. For example:
	 * 
	 * <pre>
	 * int x = 10; // assigns the value 10 to variable x
	 * </pre>
	 */

	// == Equal operator

	/**
	 * Equality operator (`==`) checks if two operands are equal. It returns `true`
	 * if they are equal, otherwise `false`. For example:
	 * 
	 * <pre>
	 * if (x == 10) {
	 * 	// code to execute if x is equal to 10
	 * }
	 * </pre>
	 */

	// != Not Equal operator

	/**
	 * Not equal operator (`!=`) checks if two operands are not equal. It returns
	 * `true` if they are not equal, otherwise `false`. For example:
	 * 
	 * <pre>
	 * if (x != 5) {
	 * 	// code to execute if x is not equal to 5
	 * }
	 * </pre>
	 */

	// > Greater Than operator

	/**
	 * Greater than operator (`>`) checks if the left operand is greater than the
	 * right operand. It returns `true` if the condition is met. For example:
	 * 
	 * <pre>
	 * if (x > 5) {
	 * 	// code to execute if x is greater than 5
	 * }
	 * </pre>
	 */

	// < Less Than operator

	/**
	 * Less than operator (`<`) checks if the left operand is less than the right
	 * operand. It returns `true` if the condition is met. For example:
	 * 
	 * <pre>
	 * if (x < 10) {
	 * 	// code to execute if x is less than 10
	 * }
	 * </pre>
	 */

	// <= Less Than or Equal operator

	/**
	 * Less than or equal to operator (`<=`) checks if the left operand is less than
	 * or equal to the right operand. It returns `true` if the condition is met. For
	 * example:
	 * 
	 * <pre>
	 * if (x <= 10) {
	 * 	// code to execute if x is less than or equal to 10
	 * }
	 * </pre>
	 */

	// >= Greater Than or Equal operator

	/**
	 * Greater than or equal to operator (`>=`) checks if the left operand is
	 * greater than or equal to the right operand. It returns `true` if the
	 * condition is met. For example:
	 * 
	 * <pre>
	 * if (x >= 5) {
	 * 	// code to execute if x is greater than or equal to 5
	 * }
	 * </pre>
	 */

	// && And operator

	/**
	 * Logical AND operator (`&&`) returns `true` if both operands are `true`. It is
	 * commonly used in conditional expressions. For example:
	 * 
	 * <pre>
	 * if (x > 5 && x < 10) {
	 * 	// code to execute if both conditions are true
	 * }
	 * </pre>
	 */

	// || Or operator

	/**
	 * Logical OR operator (`||`) returns `true` if at least one of the operands is
	 * `true`. It is commonly used in conditional expressions. For example:
	 * 
	 * <pre>
	 * if (x > 5 || x < 3) {
	 * 	// code to execute if either condition is true
	 * }
	 * </pre>
	 */

	// + Addition operator

	/**
	 * The addition operator (`+`) adds two operands together. It can also be used
	 * for concatenation of strings. For example:
	 * 
	 * <pre>
	 * int sum = 10 + 5; // result is 15
	 * String message = "Hello, " + "World!"; // concatenates two strings
	 * </pre>
	 */

	// - Subtraction operator

	/**
	 * The subtraction operator (`-`) subtracts the right-hand operand from the
	 * left-hand operand. For example:
	 * 
	 * <pre>
	 * int difference = 10 - 5; // result is 5
	 * </pre>
	 */

	// * Multiplication operator

	/**
	 * The multiplication operator (`*`) multiplies two operands. For example:
	 * 
	 * <pre>
	 * int product = 10 * 5; // result is 50
	 * </pre>
	 */

	// / Division operator

	/**
	 * The division operator (`/`) divides the left-hand operand by the right-hand
	 * operand. For example:
	 * 
	 * <pre>
	 * int quotient = 10 / 2; // result is 5
	 * </pre>
	 */

	// % Modulus operator

	/**
	 * The modulus operator (`%`) returns the remainder when the left-hand operand
	 * is divided by the right-hand operand. For example:
	 * 
	 * <pre>
	 * int remainder = 10 % 3; // result is 1
	 * </pre>
	 */

	// & Bitwise AND operator

	/**
	 * The bitwise AND operator (`&`) performs a binary AND operation on two
	 * operands. For example:
	 * 
	 * <pre>
	 * int result = 5 & 3; // result is 1 (binary: 0101 & 0011 = 0001)
	 * </pre>
	 */

	// | Bitwise OR operator

	/**
	 * The bitwise OR operator (`|`) performs a binary OR operation on two operands.
	 * For example:
	 * 
	 * <pre>
	 * int result = 5 | 3; // result is 7 (binary: 0101 | 0011 = 0111)
	 * </pre>
	 */

	// ^ Bitwise XOR operator

	/**
	 * The bitwise XOR operator (`^`) performs a binary exclusive OR operation on
	 * two operands. For example:
	 * 
	 * <pre>
	 * int result = 5 ^ 3; // result is 6 (binary: 0101 ^ 0011 = 0110)
	 * </pre>
	 */

	// << Left Shift operator

	/**
	 * The left shift operator (`<<`) shifts the bits of the left-hand operand to
	 * the left by the number of positions specified by the right-hand operand. For
	 * example:
	 * 
	 * <pre>
	 * int result = 5 << 2; // result is 20 (binary: 0101 << 2 = 10100)
	 * </pre>
	 */

	// >> Right Shift operator

	/**
	 * The right shift operator (`>>`) shifts the bits of the left-hand operand to
	 * the right by the number of positions specified by the right-hand operand. For
	 * example:
	 * 
	 * <pre>
	 * int result = 20 >> 2; // result is 5 (binary: 10100 >> 2 = 0101)
	 * </pre>
	 */

}
