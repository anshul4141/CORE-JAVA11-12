package com.rays.basic;

public class SwitchCase {

	public static void main(String[] args) {

		int month = 4;

		switch (month) {
		case 1:
			System.out.println("jan");
			break;
		case 2:
			System.out.println("feb");
			break;
		case 3:
			System.out.println("march");
			break;

		default:
			System.out.println("this is default");
			break;
		}

	}

}
